int main(void){
	/* MCU Configuration--------------------------------------------------------*/
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();
	/* Configure the system clock */
	SystemClock_Config();
	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_TIM2_Init();
	MX_TIM3_Init();
	/* Timer for Encoder */
	HAL_TIM_Encoder_Start_IT(&htim3, TIM_CHANNEL_1 | TIM_CHANNEL_2);
	HAL_TIM_Base_Start(&htim3);
	/* Timer for PWM */
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
	/* SERVO */
	SERVO_t servo1;
	SERVO_init(&servo1, &htim2);

	/* Infinite loop */
	while (1){
		// Get Encoder Value from timer
		encoder_val = TIM3->CNT;

		// UART TX
		sprintf(uart_data_buff, "\r%d\n", encoder_val/4);
		HAL_UART_Transmit(&huart2, (uint8_t *)uart_data_buff, sizeof(uart_data_buff), HAL_MAX_DELAY);

		// Set servo degree
		SERVO_set_degree(&servo1, (int)(encoder_val/4));

		// Delay
		HAL_Delay(100);
	}
}

