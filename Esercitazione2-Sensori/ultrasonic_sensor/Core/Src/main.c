/* Private variables ---------------------------------------------------------*/
TIM_HandleTypeDef htim1;
UART_HandleTypeDef huart2;

uint32_t echo_time, sensor_time;
float distance;
char uart_data_buff[10]; //Fixed Size!

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
